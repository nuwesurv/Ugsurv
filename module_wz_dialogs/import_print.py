    def add_align_point(self, x, y):
        self.terminal_dock.commandOutputText += f'\nAlign_point: {[x, y]}'
        self.terminal_dock.commandDisplay.setText(self.terminal_dock.commandOutputText)
        
        
        self.add_textedit_at(x, y)






    def add_textedit_at(self, x, y):
        # ---- Container widget ----
        container = QWidget()
        container.setFixedHeight(60)
        layout = QHBoxLayout(container)
        layout.setContentsMargins(0, 0, 0, 0)
        layout.setSpacing(5)

        # ---- QTextEdit ----
        text_edit = QLineEdit()
        # text_edit = QTextEdit()
        text_edit.setPlaceholderText("E,N")
        text_edit.setFixedSize(270, 60)

        font = QFont()
        font.setPointSize(25)
        font.setBold(True)
        font.setFamily("Arial")
        text_edit.setFont(font)

        text_edit.setStyleSheet("""
            QLineEdit {
                color: blue;
                background-color: white;
            }
        """)

        # ---- Delete Button ----
        delete_btn = QPushButton()
        delete_btn.setFixedSize(50, 60)

        # Use system trash icon
        delete_btn.setIcon(self.style().standardIcon(QPushButton().style().SP_TrashIcon))
        delete_btn.setCursor(Qt.PointingHandCursor)
        
        delete_btn.setStyleSheet("""
                QPushButton {
                    border: none;
                }
                QPushButton:hover {
                    background-color: #ffdddd;
                }
            """)

        # Add widgets to layout
        layout.addWidget(text_edit)
        layout.addWidget(delete_btn)

        # ---- Wrap in proxy ----
        proxy = QGraphicsProxyWidget()
        proxy.setWidget(container)

        self.scene.addItem(proxy)
        proxy.setPos(x, y)
        
        proxy.setFlag(proxy.ItemIsMovable, True)
        
        # 🔥 Store reference
        point_data = {
            "xy": [x,y],
            "text_edit": text_edit,
        }
        self.align_points.append(point_data)

        # ---- Delete logic ----
        def remove_widget():
            self.scene.removeItem(proxy)
            proxy.deleteLater()
            
            # remove from list
            self.align_points.remove(point_data)

        # Connect this function to the delete signal.
        delete_btn.clicked.connect(remove_widget)