    def add_action(
        self,
        icon_path,
        text,
        callback,
        enabled_flag=True,
        add_to_menu=True,
        add_to_toolbar=True,
        status_tip=None,
        whats_this=None,
        parent=None):
        """Add a toolbar icon to the toolbar.

        :param icon_path: Path to the icon for this action. Can be a resource
            path (e.g. ':/plugins/foo/bar.png') or a normal file system path.
        :type icon_path: str

        :param text: Text that should be shown in menu items for this action.
        :type text: str

        :param callback: Function to be called when the action is triggered.
        :type callback: function

        :param enabled_flag: A flag indicating if the action should be enabled
            by default. Defaults to True.
        :type enabled_flag: bool

        :param add_to_menu: Flag indicating whether the action should also
            be added to the menu. Defaults to True.
        :type add_to_menu: bool

        :param add_to_toolbar: Flag indicating whether the action should also
            be added to the toolbar. Defaults to True.
        :type add_to_toolbar: bool

        :param status_tip: Optional text to show in a popup when mouse pointer
            hovers over the action.
        :type status_tip: str

        :param parent: Parent widget for the new action. Defaults None.
        :type parent: QWidget

        :param whats_this: Optional text to show in the status bar when the
            mouse pointer hovers over the action.

        :returns: The action that was created. Note that the action is also
            added to self.actions list.
        :rtype: QAction
        """

        icon = QIcon(icon_path)
        action = QAction(icon, text, parent)
        action.triggered.connect(callback)
        action.setEnabled(enabled_flag)

        if status_tip is not None:
            action.setStatusTip(status_tip)

        if whats_this is not None:
            action.setWhatsThis(whats_this)

        if add_to_toolbar:
            # Adds plugin icon to Plugins toolbar
            self.iface.addToolBarIcon(action)

        if add_to_menu:
            self.iface.addPluginToMenu(
                self.menu,
                action)
        self.actions.append(action)
        
        action.setCheckable(True)  # <-- Make it toggleable
        return action

    def initGui(self):
        """Create the menu entries and toolbar icons inside the QGIS GUI."""

        icon_path = os.path.join(self.plugin_dir, "icon.png")
        self.add_action(
            icon_path,
            text=self.tr(u'Ugsurv'),
            callback=self.run,
            parent=self.iface.mainWindow())

        # will be set False in run()
        self.first_start = True


    def unload(self):
        """Removes the plugin menu item and icon from QGIS GUI."""
        # Removes the plugin icon from the Tool bar.
        for action in self.actions:
            self.iface.removePluginMenu(
                self.tr(u'&ParcelPro'),
                action)
            self.iface.removeToolBarIcon(action)
        
        # Remove all tools
        self.destroyAllTools()


    def run(self):
        """Run method that performs all the real work"""
        if self.active:
            # Plugin is already active → deactivate
            self.destroyAllTools()  # deactivate map tools
            self.active = False
            return  # Exit early
        
        # If we get here → plugin is inactive → activate it
        self.active = True
        
        # 🔥 remove old dock if it already exists (important for reloads)
        if hasattr(self, "terminal_dock") and self.terminal_dock:
            self.destroyAllTools()
            self.terminal_dock = None

        # terminal dock
        self.terminal_dock = TerminalDialog(self.iface.mainWindow())
        self.terminal_dock.set_commands([
            # Drawing tools (AutoCAD-standard names)
            'CIRCLE', 'PLINE', 'DIM', 'ADIM', 'TRIM',
            # Survey tools
            'TOPO', 'FIXG', 'PARCEL', 'ADDGEOM', 'CRS',
            'SPIKY', 'PTOVERLAP', 'IMPORT', 'GAME',
            # Terminal
            'HELP', 'CLEAR',
        ])
        self.iface.addDockWidget(Qt.BottomDockWidgetArea, self.terminal_dock)
        self.terminal_dock.command.setFocus()
        
        # Connect these functions to ui
        self.terminal_dock.command.returnPressed.connect(self.acceptInput)

        # Global Esc shortcut — fires regardless of which widget has focus,
        # so Esc always cancels the active map tool even when the terminal is focused.
        self.esc_shortcut = QShortcut(QKeySequence(Qt.Key_Escape), self.iface.mainWindow())
        self.esc_shortcut.activated.connect(self._on_escape)

        # This will help to setup the required snap settings.
        snapSettingConfig()

        # Install the single permanent map tool — stays on canvas for the whole session.
        # All drawing tools connect to it as delegates instead of owning the canvas.
        self.global_map_tool = UgsurvMaptool(self.canvas, self.terminal_dock)
        self.canvas.setMapTool(self.global_map_tool)

        # Vertex editor is always active when no drawing tool is running.
        vertex_selector = VertexSelector(self.canvas, self.terminal_dock)
        self.global_map_tool.set_default_tool(vertex_selector)

        from .package_installer import solve_dependency_issues
        
        # Running this function so as to install all the dependecies required.
        all_installed = solve_dependency_issues()
        print(all_installed)
        
    
    
    def _on_escape(self):
        """Forward Escape to the active tool (including the default vertex editor)."""
        if not hasattr(self, 'global_map_tool'):
            return
        mt = self.global_map_tool
        active = mt._active_tool
        if active and active is not mt._default_tool:
            # A drawing tool is running — evict it (Escape exits the tool)
            mt._evict()
        elif active and active is mt._default_tool:
            # Vertex editor is running — let it handle Escape internally
            from qgis.PyQt.QtCore import QEvent
            from qgis.PyQt.QtGui import QKeyEvent
            fake = QKeyEvent(QEvent.KeyPress, Qt.Key_Escape, Qt.NoModifier)
            active.keyPressEvent(fake)

    def acceptInput(self):
        text = self.terminal_dock.command.text()

        # common prologue — echo input and update history
        self.terminal_dock.commandHistory.insert(-1, text)
        self.terminal_dock.historyIndex = len(self.terminal_dock.commandHistory) - 1
        self.terminal_dock.commandOutputText += f'\n>>> {text}'
        self.terminal_dock.command.setText('')
        self.terminal_dock.commandDisplay.setText(self.terminal_dock.commandOutputText)

        # If a tool is waiting for typed input, hand off and stop here
        if self.terminal_dock.active_input_handler:
            handler = self.terminal_dock.active_input_handler
            self.terminal_dock.clear_input_handler()
            handler(text)
            return

        self.prevCommand = text.strip()

        # Empty Enter → repeat last command (AutoCAD behaviour)
        if not self.prevCommand:
            for cmd in reversed(self.terminal_dock.commandHistory):
                if cmd.strip():
                    self.prevCommand = cmd.strip()
                    self.terminal_dock.commandOutputText += f'  [↑ {self.prevCommand}]'
                    self.terminal_dock.commandDisplay.setText(self.terminal_dock.commandOutputText)
                    break
            else:
                return  # nothing in history to repeat

        try:
            if self.prevCommand.lower() != 'help':
                answer = ast.literal_eval(self.prevCommand)
                self.terminal_dock.commandOutputText += f' = {answer}'
        except Exception:
            ...

        self.terminal_dock.commandDisplay.setText(self.terminal_dock.commandOutputText)
        
        cmd = self.prevCommand.lower()

        if cmd in ('help', '?'):
            self.terminal_dock.commandOutputText += (
                "\n── Drawing ──────────────────────────"
                "\n  CIRCLE  [C ]   Draw a circle"
                "\n  PLINE   [PL]   Draw a polyline"
                "\n  TRIM    [TR]   Trim lines at cutting edges"
                "\n  DIM     [DI]   Dimension a segment / two points"
                "\n  ADIM    [AD]   Dimension all segments of a feature"
                "\n── Vertex editor (always active) ────"
                "\n  click vertex   Grip it (1st click)"
                "\n  click grip     Start move (2nd click)"
                "\n  click to place Commit move"
                "\n  Del            Delete gripped vertex"
                "\n  Esc            Clear grip / cancel move"
                "\n── Survey tools ──────────────────────"
                "\n  TOPO    [TS]   Topology solver"
                "\n  FIXG    [FG]   Fix geometry"
                "\n  PARCEL  [PP]   Auto-plot parcels"
                "\n  ADDGEOM [GA]   Add geometry to layer"
                "\n  CRS            Adjust CRS of selected features"
                "\n  SPIKY   [SPK]  Detect spiky vertices"
                "\n  PTOVERLAP[PTO] Detect overlapping boundary points"
                "\n  IMPORT  [IMP]  Import & georeference cadastral print"
                "\n  GAME    [GM]   Total-station laser game"
                "\n── Terminal ──────────────────────────"
                "\n  CLEAR   [CLS]  Clear terminal"
                "\n  HELP    [?  ]  Show this list"
                "\n"
            )
            self.terminal_dock.commandDisplay.setText(self.terminal_dock.commandOutputText)

        elif cmd in ('clear', 'cls'):
            self.terminal_dock.commandHistory = ['']
            self.terminal_dock.historyIndex = 0
            self.terminal_dock.commandOutputText = 'Terminal cleared.'
            self.terminal_dock.commandDisplay.setText(self.terminal_dock.commandOutputText)

        elif cmd in ('circle', 'c'):
            self.global_map_tool.set_tool(CircleDrawer(self.canvas, self.terminal_dock))

        elif cmd in ('pline', 'pl'):
            self.global_map_tool.set_tool(PolylineDrawer(self.canvas, self.terminal_dock))

        elif cmd in ('trim', 'tr'):
            self.global_map_tool.set_tool(TrimTool(self.canvas, self.terminal_dock))

        elif cmd in ('dim', 'di'):
            self.global_map_tool.set_tool(DimensionDrawer(self.canvas, self.terminal_dock, 'single'))

        elif cmd in ('adim', 'ad'):
            self.global_map_tool.set_tool(DimensionDrawer(self.canvas, self.terminal_dock, 'selected'))

        elif cmd in ('topo', 'ts'):
            self.global_map_tool.set_tool(TopologySolver(self.canvas, self.iface, self.terminal_dock))

        elif cmd in ('fixg', 'fg'):
            self.global_map_tool.set_tool(FixGeometry(self.canvas, self.terminal_dock))

        elif cmd in ('parcel', 'pp'):
            self.dlg = ParcelPlotterDialog(parent=self.iface.mainWindow())
            self.dlg.show()

        elif cmd in ('addgeom', 'ga'):
            self.append_geom_dock = GeometryAppenderDock(self.iface.mainWindow())
            self.iface.addDockWidget(Qt.LeftDockWidgetArea, self.append_geom_dock)

        elif cmd == 'crs':
            self.crs_adjust_dock = CrsAdjustDock(self.iface.mainWindow())
            self.iface.addDockWidget(Qt.LeftDockWidgetArea, self.crs_adjust_dock)

        elif cmd in ('spiky', 'spk'):
            self.spiky_detect_dock = SpikyGeomsDock(self.canvas, self.iface.mainWindow())
            self.iface.addDockWidget(Qt.LeftDockWidgetArea, self.spiky_detect_dock)

        elif cmd in ('ptoverlap', 'pto', 'pt_overlap'):
            self.overlap_detect_dock = OverlapPointsDock(self.canvas, self.iface.mainWindow())
            self.iface.addDockWidget(Qt.LeftDockWidgetArea, self.overlap_detect_dock)

        elif cmd in ('import', 'imp', 'print'):
            self.dlg = ImportPrintDialog(terminal_dock=self.terminal_dock)
            self.dlg.show()

        elif cmd in ('game', 'gm'):
            self.global_map_tool.set_tool(Game1(self.canvas, self.terminal_dock))
            
            
            
            
            
            
            
    def destroyAllTools(self):
        # Remove global Esc shortcut
        try:
            self.esc_shortcut.activated.disconnect()
            self.esc_shortcut.deleteLater()
            self.esc_shortcut = None
        except Exception:
            pass

        # Evict any active delegate, then release the canvas tool
        try:
            self.global_map_tool._evict()
            self.canvas.unsetMapTool(self.global_map_tool)
            self.global_map_tool = None
        except Exception:
            pass
        
        # Remove any dialog boxes
        try:
            self.dlg.close()
            self.dlg.deleteLater()
            self.dlg = None
        except:
            pass
        
        
        # Remove the spiky detector
        try:
            self.iface.removeDockWidget(self.spiky_detect_dock)
            self.spiky_detect_dock.deleteLater()
        except:
            pass

        # Remove the overlap detector
        try:
            self.iface.removeDockWidget(self.overlap_detect_dock)
            self.overlap_detect_dock.deleteLater()
        except:
            pass

        # Remove the CRS adjust dock
        try:
            self.iface.removeDockWidget(self.crs_adjust_dock)
            self.crs_adjust_dock.deleteLater()
        except:
            pass

        # Remove the append geometry dock
        try:
            self.iface.removeDockWidget(self.append_geom_dock)
            self.append_geom_dock.deleteLater()
        except:
            pass
        
        # Remove the terminal
        try:
            self.iface.removeDockWidget(self.terminal_dock)
            self.terminal_dock.deleteLater()
        except:
            pass
            
            
            
            