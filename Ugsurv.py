    # noinspection PyMethodMayBeStatic
    def tr(self, message):
        """Get the translation for a string using Qt translation API.

        We implement this ourselves since we do not inherit QObject.

        :param message: String for translation.
        :type message: str, QString

        :returns: Translated version of message.
        :rtype: QString
        """
        # noinspection PyTypeChecker,PyArgumentList,PyCallByClass
        return QCoreApplication.translate('Ugsurv', message)


    def add_action(
        self,
        icon_path,
        text,
        callback,
        enabled_flag=True,
        add_to_menu=True,
        add_to_toolbar=True,
        status_tip=None,
        whats_this=None,
        parent=None):
        """Add a toolbar icon to the toolbar.

        :param icon_path: Path to the icon for this action. Can be a resource
            path (e.g. ':/plugins/foo/bar.png') or a normal file system path.
        :type icon_path: str

        :param text: Text that should be shown in menu items for this action.
        :type text: str

        :param callback: Function to be called when the action is triggered.
        :type callback: function

        :param enabled_flag: A flag indicating if the action should be enabled
            by default. Defaults to True.
        :type enabled_flag: bool

        :param add_to_menu: Flag indicating whether the action should also
            be added to the menu. Defaults to True.
        :type add_to_menu: bool

        :param add_to_toolbar: Flag indicating whether the action should also
            be added to the toolbar. Defaults to True.
        :type add_to_toolbar: bool

        :param status_tip: Optional text to show in a popup when mouse pointer
            hovers over the action.
        :type status_tip: str

        :param parent: Parent widget for the new action. Defaults None.
        :type parent: QWidget

        :param whats_this: Optional text to show in the status bar when the
            mouse pointer hovers over the action.

        :returns: The action that was created. Note that the action is also
            added to self.actions list.
        :rtype: QAction
        """

        icon = QIcon(icon_path)
        action = QAction(icon, text, parent)
        action.triggered.connect(callback)
        action.setEnabled(enabled_flag)

        if status_tip is not None:
            action.setStatusTip(status_tip)

        if whats_this is not None:
            action.setWhatsThis(whats_this)

        if add_to_toolbar:
            # Adds plugin icon to Plugins toolbar
            self.iface.addToolBarIcon(action)

        if add_to_menu:
            self.iface.addPluginToMenu(
                self.menu,
                action)

        self.actions.append(action)

        return action

    def initGui(self):
        """Create the menu entries and toolbar icons inside the QGIS GUI."""

        icon_path = os.path.join(self.plugin_dir, "icon.png")
        self.add_action(
            icon_path,
            text=self.tr(u'Ugsurv'),
            callback=self.run,
            parent=self.iface.mainWindow())

        # will be set False in run()
        self.first_start = True


    def unload(self):
        """Removes the plugin menu item and icon from QGIS GUI."""
        # Removes the plugin icon from the Tool bar.
        for action in self.actions:
            self.iface.removePluginMenu(
                self.tr(u'&ParcelPro'),
                action)
            self.iface.removeToolBarIcon(action)
        # Removes the plugin from the docks.
        if hasattr(self, "terminal_dock") and self.terminal_dock:
            self.stopMapTools()
            self.iface.removeDockWidget(self.terminal_dock)
            self.terminal_dock.deleteLater()
            self.terminal_dock = None


    def run(self):
        """Run method that performs all the real work"""

        # 🔥 remove old dock if it already exists (important for reloads)
        if hasattr(self, "terminal_dock") and self.terminal_dock:
            try:
                self.iface.removeDockWidget(self.terminal_dock)
                self.terminal_dock.deleteLater()
            except:
                pass
            self.terminal_dock = None

        # terminal dock
        self.terminal_dock = TerminalDialog(self.iface.mainWindow())
        self.iface.addDockWidget(Qt.BottomDockWidgetArea, self.terminal_dock)
        
        # self.createScratchLayer()  # ✅ add this
        
        # Connect these functions to ui
        self.terminal_dock.command.returnPressed.connect(self.acceptInput)
        
        # in your run() after creating the dock:
        shortcut_escape = QShortcut(QKeySequence("Escape"), self.terminal_dock)
        shortcut_escape.activated.connect(self.stopMapTools)
        
    
    
    def acceptInput(self):
        # update the history index and the commandHistory
        self.terminal_dock.commandHistory.insert(-1,self.terminal_dock.command.text())
        self.terminal_dock.historyIndex = len(self.terminal_dock.commandHistory) - 1
        
        self.prevCommand = self.terminal_dock.command.text()
        self.terminal_dock.commandOutputText += f'\n>>> {self.terminal_dock.command.text()}'
        self.terminal_dock.command.setText('')
        
        try:
            answer = eval(self.prevCommand)
            self.terminal_dock.commandOutputText += f' = {answer}'
        except Exception as e:
            # print(f"{self.prevCommand} can't be evaluated")
            ...
        
        # first update the input command i the display
        self.terminal_dock.commandDisplay.setText(self.terminal_dock.commandOutputText)
        
        # =================================================================================================
        # then caryyout operations.
        # our first command is tracking the cursor cordinates.
        if self.prevCommand == 'track':
            self.currentcommand = 'track'
            self.canvas.setMapTool(self.tracker)
            def cursorCordsStream(x, y):
                self.terminal_dock.commandDisplay.setText(self.terminal_dock.commandOutputText + f'\ncursor_xy: {round(x,3)}, {round(y,3)}\n' )
                # print(x, y)
            
            # connect signal to your handler
            self.tracker.cursor_cords.connect(cursorCordsStream)
            
        # Second command is for unsetting the cursor cordinates tracker
        self.command_list.append('untrack')
        if self.prevCommand == 'untrack':
            self.canvas.unsetMapTool(self.tracker)
            
        # Second command is for unsetting the cursor cordinates tracker
        if self.prevCommand == 'cls':
            self.terminal_dock.commandOutputText = 'Terminal cleared...'
            self.terminal_dock.commandDisplay.setText('Terminal cleared...')
            
            
        # The above functions are gimmicky just for demo purposes
        # The function we have here below is for drawing a circle at the cursor cords
        if self.prevCommand == 'circle':
            # step1: get the center where cordinates shall be placed.
            circle_points = []
            circle_radius = 0
            self.canvas.setMapTool(self.tracker)
            def cursorCordsStream(x, y):
                self.cursor_cords = [x,y]
                if circle_points.__len__() == 0:
                    self.terminal_dock.commandDisplay.setText(self.terminal_dock.commandOutputText + f'\nSelect circle center: {round(x,3)}, {round(y,3)}\n' )
                if circle_points.__len__() == 1:
                    circle_radius = math.sqrt((circle_points[0][0]-self.cursor_cords[0])**2 + (circle_points[0][1]-self.cursor_cords[1])**2)
                    self.terminal_dock.commandDisplay.setText(self.terminal_dock.commandOutputText + f'\nCircle radius: {round(circle_radius,3)}\n' )
            def handleLeftCanvasClick(x, y):
                circle_points.append([x, y])
                if circle_points.__len__() == 1:
                    self.terminal_dock.commandOutputText += f'\nCircle center: {round(x,3)}, {round(y,3)}'
                    self.terminal_dock.commandDisplay.setText(self.terminal_dock.commandOutputText + f'\nSelect circle radius: ... \n' )
                if circle_points.__len__() == 2:
                    circle_radius = math.sqrt((circle_points[0][0]-circle_points[1][0])**2 + (circle_points[0][1]-circle_points[1][1])**2)
                    self.terminal_dock.commandOutputText += f'\nCircle radius: {round(circle_radius,3)}'
                    self.terminal_dock.commandDisplay.setText(self.terminal_dock.commandOutputText)
                    self.canvas.unsetMapTool(self.tracker)
                    # self.addCircleToLayer(circle_points[0][0], circle_points[0][1], circle_radius)
                
            # connect signal to your handler
            self.tracker.cursor_cords.connect(cursorCordsStream)
            self.tracker.leftClicked.connect(handleLeftCanvasClick)
            
        
        if self.prevCommand == 'pp':
            self.dlg = ParcelPlotterDialog()
            self.dlg.show()
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
