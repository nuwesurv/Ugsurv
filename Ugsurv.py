    @staticmethod
    def _make_snap_icon(active=True):
        """Draw a U-magnet icon; grey when all snap modes are off."""
        size = 24
        px = QPixmap(size, size)
        px.fill(Qt.transparent)
        p = QPainter(px)
        p.setRenderHint(QPainter.Antialiasing)

        arm_col = QColor(240, 165, 0) if active else QColor(130, 130, 130)
        p.setPen(QPen(arm_col, 3, Qt.SolidLine, Qt.RoundCap))
        p.drawLine(7, 4, 7, 14)
        p.drawLine(17, 4, 17, 14)
        p.drawArc(QRect(7, 9, 10, 10), 0, -180 * 16)

        lc = QColor(210, 50, 50)  if active else QColor(130, 130, 130)
        rc = QColor(50, 80, 210)  if active else QColor(130, 130, 130)
        p.setPen(QPen(lc, 3, Qt.SolidLine, Qt.SquareCap))
        p.drawLine(4, 4, 10, 4)
        p.setPen(QPen(rc, 3, Qt.SolidLine, Qt.SquareCap))
        p.drawLine(14, 4, 20, 4)
        p.end()
        return QIcon(px)

    def _create_snap_button(self):
        menu = QMenu()
        self._snap_actions = {}
        # Items listed in snap priority order (highest first)
        for key, label in [
            (snap_manager.ENDPOINT,     'Endpoint'),
            (snap_manager.CENTER,       'Center'),
            (snap_manager.MIDPOINT,     'Midpoint'),
            (snap_manager.INTERSECTION, 'Intersection'),
            (snap_manager.NEAREST,      'Nearest'),
        ]:
            act = QAction(label, menu)
            act.setCheckable(True)
            act.setChecked(snap_manager.is_enabled(key))
            act.toggled.connect(lambda checked, k=key: self._on_snap_toggle(k, checked))
            menu.addAction(act)
            self._snap_actions[key] = act

        menu.addSeparator()
        all_on  = QAction('Select All', menu)
        all_off = QAction('Clear All',  menu)
        all_on.triggered.connect(lambda: self._set_all_snaps(True))
        all_off.triggered.connect(lambda: self._set_all_snaps(False))
        menu.addAction(all_on)
        menu.addAction(all_off)

        btn = QToolButton()
        btn.setIcon(self._make_snap_icon(True))
        btn.setToolTip('Object snap options (Endpoint, Midpoint, Center, Intersection, Nearest)')
        btn.setMenu(menu)
        btn.setPopupMode(QToolButton.MenuButtonPopup)
        btn.clicked.connect(btn.showMenu)
        return btn

    def _on_snap_toggle(self, key, checked):
        snap_manager.set_enabled(key, checked)
        any_on = any(a.isChecked() for a in self._snap_actions.values())
        self._snap_btn.setIcon(self._make_snap_icon(any_on))

    def _set_all_snaps(self, state):
        for act in self._snap_actions.values():
            act.setChecked(state)   # triggers _on_snap_toggle via toggled signal

    def initGui(self):
        """Create the menu entries and toolbar icons inside the QGIS GUI."""

        icon_path = os.path.join(self.plugin_dir, "icon.png")
        self.add_action(
            icon_path,
            text=self.tr(u'Ugsurv'),
            callback=self.run,
            parent=self.iface.mainWindow())

        # Second button: restore the UgSurv map tool cursor after switching to a QGIS tool
        self._maptool_action = QAction(
            self._maptool_icon(),
            self.tr(u'Activate UgSurv Map Tool'),
            self.iface.mainWindow()
        )
        self._maptool_action.setToolTip('Activate UgSurv map tool')
        self._maptool_action.setCheckable(True)
        self._maptool_action.setChecked(False)
        self._maptool_action.triggered.connect(self.activate_maptool)
        self.iface.addToolBarIcon(self._maptool_action)
        self.actions.append(self._maptool_action)

        # Snap options dropdown (magnet button)
        self._snap_btn = self._create_snap_button()
        self.iface.pluginToolBar().addWidget(self._snap_btn)

        # will be set False in run()
        self.first_start = True


    def unload(self):
        """Removes the plugin menu item and icon from QGIS GUI."""
        for action in self.actions:
            self.iface.removePluginMenu(
                self.tr(u'&ParcelPro'),
                action)
            self.iface.removeToolBarIcon(action)

        # Remove snap dropdown button
        try:
            self._snap_btn.deleteLater()
        except Exception:
            pass

        # Remove all tools
        self.destroyAllTools()


    def run(self):
        """Run method that performs all the real work"""
        if self.active:
            # Plugin is already active → deactivate
            self.destroyAllTools()  # deactivate map tools
            self.active = False
            return  # Exit early
        
        # If we get here → plugin is inactive → activate it
        self.active = True
        
        # 🔥 remove old dock if it already exists (important for reloads)
        if hasattr(self, "terminal_dock") and self.terminal_dock:
            self.destroyAllTools()
            self.terminal_dock = None

        # terminal dock
        self.terminal_dock = TerminalDialog(self.iface.mainWindow())
        self.terminal_dock.set_commands([
            # Drawing tools (AutoCAD-standard names)
            'CIRCLE', 'POINT', 'PLINE', 'DIM', 'ADIM',
            'MOVE', 'COPY', 'ROTATE', 'SCALE', 'OFFSET',
            'TRIM', 'EXTEND', 'JOIN',
            'BREAK', 'CHAMFER', 'EXPLODE',
            # Survey tools
            'TOPO', 'FIXG', 'PARCEL', 'ADDGEOM', 'CRS',
            'SPIKY', 'PTOVERLAP', 'IMPORT', 'GAME',
            # Terminal
            'HELP', 'CLEAR',
        ])
        self.iface.addDockWidget(Qt.BottomDockWidgetArea, self.terminal_dock)
        self.terminal_dock.command.setFocus()
        
        # Connect these functions to ui
        self.terminal_dock.command.returnPressed.connect(self.acceptInput)

        # Global Esc shortcut — fires regardless of which widget has focus,
        # so Esc always cancels the active map tool even when the terminal is focused.
        self.esc_shortcut = QShortcut(QKeySequence(Qt.Key_Escape), self.iface.mainWindow())
        self.esc_shortcut.activated.connect(self._on_escape)

        # This will help to setup the required snap settings.
        init_snap()

        # Install the single permanent map tool — stays on canvas for the whole session.
        # All drawing tools connect to it as delegates instead of owning the canvas.
        self.global_map_tool = UgsurvMaptool(self.canvas, self.terminal_dock)
        self.canvas.setMapTool(self.global_map_tool)
        self._maptool_action.setChecked(True)
        self.canvas.mapToolSet.connect(self._on_map_tool_set)

        # Allow the terminal to restore our map tool when the user clicks it
        # (e.g. after using a QGIS built-in tool like Identify).
        self.terminal_dock.on_activate_maptool = lambda: self.canvas.setMapTool(self.global_map_tool)
        self.terminal_dock.on_canvas_key = lambda e: QApplication.sendEvent(self.canvas, e)

        # Vertex editor is always active when no drawing tool is running.
        vertex_selector = VertexSelector(self.canvas, self.terminal_dock)
        self.global_map_tool.set_default_tool(vertex_selector)

        from .package_installer import solve_dependency_issues
        
        # Running this function so as to install all the dependecies required.
        all_installed = solve_dependency_issues()
        print(all_installed)
        
    
    
    def _on_escape(self):
        """Forward Escape to the active tool (including the default vertex editor)."""
        if not hasattr(self, 'global_map_tool'):
            return
        mt = self.global_map_tool
        active = mt._active_tool
        if active and active is not mt._default_tool:
            # A drawing tool is running — evict it (Escape exits the tool)
            mt._evict()
        elif active and active is mt._default_tool:
            # Vertex editor is running — let it handle Escape internally
            from qgis.PyQt.QtCore import QEvent
            from qgis.PyQt.QtGui import QKeyEvent
            fake = QKeyEvent(QEvent.KeyPress, Qt.Key_Escape, Qt.NoModifier)
            active.keyPressEvent(fake)

        # Always clear any partially typed text in the terminal command field
        if hasattr(self, 'terminal_dock') and self.terminal_dock:
            self.terminal_dock.command.clear()

    def acceptInput(self):
        # If a QGIS built-in tool displaced ours, reclaim the canvas before acting.
        if hasattr(self, 'global_map_tool') and self.global_map_tool:
            self.canvas.setMapTool(self.global_map_tool)

        text = self.terminal_dock.command.text()

        # common prologue — echo input and update history
        self.terminal_dock.commandHistory.insert(-1, text)
        self.terminal_dock.historyIndex = len(self.terminal_dock.commandHistory) - 1
        self.terminal_dock.commandOutputText += f'\n>>> {text}'
        self.terminal_dock.command.setText('')
        self.terminal_dock.commandDisplay.setText(self.terminal_dock.commandOutputText)

        # If a tool is waiting for typed input, hand off and stop here
        if self.terminal_dock.active_input_handler:
            handler = self.terminal_dock.active_input_handler
            self.terminal_dock.clear_input_handler()
            handler(text)
            return

        self.prevCommand = text.strip()

        # Empty Enter → forward to the active drawing tool if one is running,
        # otherwise repeat the last command (AutoCAD behaviour).
        # Without this guard, acceptInput() would re-launch the tool command,
        # evicting the running instance before its keyPressEvent ever fired.
        if not self.prevCommand:
            mt = getattr(self, 'global_map_tool', None)
            if (mt and mt._active_tool
                    and mt._active_tool is not getattr(mt, '_default_tool', None)):
                from qgis.PyQt.QtCore import QEvent
                from qgis.PyQt.QtGui import QKeyEvent
                fake = QKeyEvent(QEvent.KeyPress, Qt.Key_Return, Qt.NoModifier)
                mt._active_tool.keyPressEvent(fake)
                return

        if not self.prevCommand:
            for cmd in reversed(self.terminal_dock.commandHistory):
                if cmd.strip():
                    self.prevCommand = cmd.strip()
                    self.terminal_dock.commandOutputText += f'  [↑ {self.prevCommand}]'
                    self.terminal_dock.commandDisplay.setText(self.terminal_dock.commandOutputText)
                    break
            else:
                return  # nothing in history to repeat

