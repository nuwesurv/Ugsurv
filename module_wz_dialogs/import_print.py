class ZoomableView(QGraphicsView):
    # 👇 custom signal (emit x, y)
    clicked = pyqtSignal(float, float)
    
    def __init__(self, parent=None):
        super().__init__(parent)

        self.setCursor(Qt.CrossCursor) # set the cursor to become a cross
        self.setTransformationAnchor(QGraphicsView.AnchorUnderMouse)
        self.setResizeAnchor(QGraphicsView.AnchorUnderMouse)
        self.setDragMode(QGraphicsView.ScrollHandDrag)

    def wheelEvent(self, event):
        zoomFactor = 1.25

        if event.angleDelta().y() > 0:
            self.scale(zoomFactor, zoomFactor)
        else:
            self.scale(1 / zoomFactor, 1 / zoomFactor)
            
    def mousePressEvent(self, event):
        if event.button() == Qt.LeftButton:
            # Convert mouse pos to scene coords
            scene_pos = self.mapToScene(event.pos())

            # Check if click is on a QGraphicsProxyWidget
            item = self.scene().itemAt(scene_pos, self.transform())
            if item is not None and isinstance(item, QGraphicsProxyWidget):
                # Clicked on a widget → ignore view click
                super().mousePressEvent(event)
                return  # don't emit clicked signal

            # Otherwise emit signal
            self.clicked.emit(scene_pos.x(), scene_pos.y())

        super().mousePressEvent(event)









